    def calculate_technical_indicators(self, df):
        """Calculate comprehensive technical indicators"""
        df = df.copy()

        try:
            # Basic price features
            df['Returns'] = df['Close'].pct_change().fillna(0)
            df['High_Low_Ratio'] = df['High'] / df['Low']
            df['Open_Close_Ratio'] = df['Open'] / df['Close']
            df['Volume_Change'] = df['Volume'].pct_change().fillna(0)

            # Price changes over multiple periods
            for period in [1, 2, 3, 5, 10]:
                df[f'Price_Change_{period}d'] = df['Close'].pct_change(period).fillna(0)

            # Moving averages
            ma_periods = [5, 10, 15, 20, 30, 50]
            for period in ma_periods:
                if len(df) > period:
                    df[f'SMA_{period}'] = df['Close'].rolling(window=period, min_periods=1).mean()
                    df[f'EMA_{period}'] = df['Close'].ewm(span=period, min_periods=1).mean()
                    df[f'Price_SMA_{period}_Ratio'] = df['Close'] / df[f'SMA_{period}']
                    df[f'Volume_SMA_{period}'] = df['Volume'].rolling(window=period, min_periods=1).mean()

            # RSI for multiple periods
            for period in [7, 14, 21, 28]:
                if len(df) > period:
                    delta = df['Close'].diff()
                    gain = delta.where(delta > 0, 0).rolling(window=period, min_periods=1).mean()
                    loss = -delta.where(delta < 0, 0).rolling(window=period, min_periods=1).mean()
                    rs = gain / (loss + 1e-10)
                    df[f'RSI_{period}'] = 100 - (100 / (1 + rs))

            # MACD - Enhanced version
            if len(df) > 26:
                df['EMA_12'] = df['Close'].ewm(span=12, min_periods=1).mean()
                df['EMA_26'] = df['Close'].ewm(span=26, min_periods=1).mean()
                df['MACD'] = df['EMA_12'] - df['EMA_26']
                df['MACD_Signal'] = df['MACD'].ewm(span=9, min_periods=1).mean()
                df['MACD_Histogram'] = df['MACD'] - df['MACD_Signal']
                df['MACD_Cross'] = (df['MACD'] > df['MACD_Signal']).astype(int)

            # Bollinger Bands
            if len(df) > 20:
                for period, std_mult in [(20, 2), (20, 2.5)]:
                    bb_middle = df['Close'].rolling(window=period, min_periods=1).mean()
                    bb_std = df['Close'].rolling(window=period, min_periods=1).std()
                    df[f'BB_Upper_{period}_{std_mult}'] = bb_middle + (bb_std * std_mult)
                    df[f'BB_Lower_{period}_{std_mult}'] = bb_middle - (bb_std * std_mult)
                    df[f'BB_Width_{period}_{std_mult}'] = ((df[f'BB_Upper_{period}_{std_mult}'] -
                                                          df[f'BB_Lower_{period}_{std_mult}']) / bb_middle)
                    df[f'BB_Position_{period}_{std_mult}'] = ((df['Close'] - df[f'BB_Lower_{period}_{std_mult}']) /
                                                             (df[f'BB_Upper_{period}_{std_mult}'] - df[f'BB_Lower_{period}_{std_mult}'] + 1e-10))

            # Stochastic Oscillator
            for period in [14, 21]:
                if len(df) > period:
                    low_min = df['Low'].rolling(window=period, min_periods=1).min()
                    high_max = df['High'].rolling(window=period, min_periods=1).max()
                    df[f'Stoch_K_{period}'] = 100 * ((df['Close'] - low_min) / (high_max - low_min + 1e-10))
                    df[f'Stoch_D_{period}'] = df[f'Stoch_K_{period}'].rolling(window=3, min_periods=1).mean()

            # Williams %R
            if len(df) > 14:
                high_max = df['High'].rolling(window=14, min_periods=1).max()
                low_min = df['Low'].rolling(window=14, min_periods=1).min()
                df['Williams_R'] = -100 * ((high_max - df['Close']) / (high_max - low_min + 1e-10))

            # Average True Range (ATR)
            if len(df) > 14:
                high_low = df['High'] - df['Low']
                high_close = abs(df['High'] - df['Close'].shift(1))
                low_close = abs(df['Low'] - df['Close'].shift(1))
                true_range = pd.concat([high_low, high_close, low_close], axis=1).max(axis=1)
                df['ATR'] = true_range.rolling(window=14, min_periods=1).mean()
                df['ATR_Ratio'] = df['ATR'] / (df['Close'] + 1e-10)

            # Volume indicators
            df['Volume_Price_Trend'] = df['Volume'] * df['Returns']
            df['On_Balance_Volume'] = (df['Volume'] * np.sign(df['Returns'])).cumsum()

            # Volatility measures
            for period in [10, 20, 30]:
                if len(df) > period:
                    df[f'Volatility_{period}'] = df['Returns'].rolling(window=period, min_periods=1).std()
                    df[f'Price_Range_{period}'] = ((df['High'].rolling(period, min_periods=1).max() -
                                                  df['Low'].rolling(period, min_periods=1).min()) / df['Close'])

            # Price position indicators
            for period in [10, 20, 50]:
                if len(df) > period:
                    df[f'Price_Position_{period}'] = ((df['Close'] - df['Low'].rolling(period, min_periods=1).min()) /
                                                     (df['High'].rolling(period, min_periods=1).max() -
                                                      df['Low'].rolling(period, min_periods=1).min() + 1e-10))

            # Momentum indicators
            for period in [5, 10, 20]:
                if len(df) > period:
                    df[f'Momentum_{period}'] = df['Close'] / df['Close'].shift(period) - 1

            # Advanced patterns
            df['Doji'] = abs(df['Close'] - df['Open']) / (df['High'] - df['Low'] + 1e-10)
            df['Upper_Shadow'] = df['High'] - df[['Open', 'Close']].max(axis=1)
            df['Lower_Shadow'] = df[['Open', 'Close']].min(axis=1) - df['Low']
            df['Body_Size'] = abs(df['Close'] - df['Open']) / (df['Close'] + 1e-10)

            # Fill any remaining NaN values
            df = df.fillna(method='ffill').fillna(method='bfill').fillna(0)

        except Exception as e:
            print(f"    Warning: Error calculating some indicators - {e}")

        return df

    def create_features(self):
        """Create feature matrix with advanced selection"""
        print("Creating enhanced features...")

        for ticker, df in self.data.items():
            try:
                print(f"  Processing {ticker}...")

                # Calculate technical indicators
                df_enhanced = self.calculate_technical_indicators(df)

                # Create target variable
                df_enhanced['Target'] = (df_enhanced['Close'].shift(-1) > df_enhanced['Close']).astype(int)

                # Select numeric features
                numeric_cols = df_enhanced.select_dtypes(include=[np.number]).columns.tolist()
                feature_cols = [col for col in numeric_cols if col not in ['Target', 'Close']]

                # Remove problematic features
                safe_features = []
                for col in feature_cols:
                    try:
                        if (not df_enhanced[col].isna().all() and
                            df_enhanced[col].std() > 1e-10 and
                            np.isfinite(df_enhanced[col]).all()):
                            safe_features.append(col)
                    except:
                        continue

                # Create feature dataframe
                features_df = df_enhanced[safe_features + ['Target']].copy()
                features_df = features_df.dropna(subset=['Target'])

                # Handle missing values
                if features_df.isnull().sum().sum() > 0:
                    for col in safe_features:
                        if features_df[col].isnull().sum() > 0:
                            features_df[col] = features_df[col].fillna(features_df[col].median())

                # Remove highly correlated features
                if len(safe_features) > 10:
                    corr_matrix = features_df[safe_features].corr().abs()
                    upper_tri = corr_matrix.where(np.triu(np.ones(corr_matrix.shape), k=1).astype(bool))
                    high_corr_features = [col for col in upper_tri.columns if any(upper_tri[col] > 0.95)]
                    safe_features = [col for col in safe_features if col not in high_corr_features]

                # Feature selection
                if len(safe_features) > 35:
                    X = features_df[safe_features]
                    y = features_df['Target']

                    selector = SelectKBest(score_func=f_classif, k=min(35, len(safe_features)))
                    X_selected = selector.fit_transform(X, y)
                    selected_features = [safe_features[i] for i in selector.get_support(indices=True)]
                else:
                    selected_features = safe_features

                # Final feature dataframe
                self.features[ticker] = features_df[selected_features + ['Target']].copy()

                print(f"    Created {len(selected_features)} features, {len(self.features[ticker])} samples")

            except Exception as e:
                print(f"    Error processing {ticker}: {e}")
                continue

        return self.features

