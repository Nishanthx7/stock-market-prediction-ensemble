    def _create_sequences(self, X, y, sequence_length):
        """Create sequences for LSTM"""
        X_seq, y_seq = [], []
        for i in range(sequence_length, len(X)):
            X_seq.append(X[i-sequence_length:i])
            y_seq.append(y[i])
        return np.array(X_seq), np.array(y_seq)

    def train_ensemble_models(self):
        """Train stacking ensemble models"""
        print("Training ensemble models...")

        for ticker in self.models.keys():
            try:
                print(f"  Creating ensembles for {ticker}...")

                # Get base model predictions
                base_predictions = []
                X_test = self.models[ticker]['X_test']
                y_test = self.models[ticker]['y_test']
                model_names = []

                # Collect base model predictions
                base_models = ['RandomForest', 'XGBoost', 'LightGBM', 'CatBoost', 'GradientBoosting', 'ExtraTrees']
                for model_name in base_models:
                    if model_name in self.models[ticker]:
                        try:
                            pred = self.models[ticker][model_name].predict(X_test)
                            base_predictions.append(pred)
                            model_names.append(model_name)
                        except:
                            continue

                # LSTM predictions
                if 'LSTM' in self.models[ticker]:
                    try:
                        X_test_seq, y_test_seq = self.models[ticker]['LSTM_test_data']
                        lstm_pred = self.models[ticker]['LSTM'].predict(X_test_seq, verbose=0)
                        lstm_pred_binary = (lstm_pred > 0.5).astype(int).flatten()

                        if len(base_predictions) > 0:
                            min_len = min(len(base_predictions[0]), len(lstm_pred_binary))
                            base_predictions = [pred[:min_len] for pred in base_predictions]
                            y_test = y_test[:min_len]
                            base_predictions.append(lstm_pred_binary[:min_len])
                            model_names.append('LSTM')
                    except:
                        pass

                if len(base_predictions) >= 2:
                    X_meta = np.column_stack(base_predictions)

                    # Simple Voting Ensemble
                    try:
                        voting_pred = (np.mean(X_meta, axis=1) > 0.5).astype(int)
                        self.models[ticker]['VotingEnsemble'] = {'predictions': voting_pred}
                        print(f"    Voting Ensemble created")
                    except:
                        pass

                    # Weighted Voting
                    try:
                        weights = []
                        for pred in base_predictions:
                            acc = accuracy_score(y_test[:len(pred)], pred)
                            weights.append(max(acc, 0.5))

                        weights = np.array(weights) / np.sum(weights)
                        weighted_pred = (np.average(X_meta, axis=1, weights=weights) > 0.5).astype(int)
                        self.models[ticker]['WeightedVoting'] = {'predictions': weighted_pred, 'weights': weights}
                        print(f"    Weighted Voting created")
                    except:
                        pass

                    # Meta Random Forest
                    try:
                        meta_rf = RandomForestClassifier(n_estimators=100, max_depth=8, random_state=42)
                        meta_rf.fit(X_meta, y_test)
                        meta_pred = meta_rf.predict(X_meta)
                        self.models[ticker]['MetaRF'] = {'model': meta_rf, 'predictions': meta_pred}
                        print(f"    Meta Random Forest created")
                    except:
                        pass

                    # Store meta data
                    self.models[ticker]['meta_data'] = {
                        'X_meta': X_meta,
                        'y_meta': y_test,
                        'base_model_names': model_names
                    }

            except Exception as e:
                print(f"    Error creating ensembles for {ticker}: {e}")

        return self.models

    def evaluate_models(self):
        """Comprehensive model evaluation"""
        print("Evaluating all models...")

        for ticker in self.models.keys():
            print(f"  Evaluating {ticker}...")
            self.results[ticker] = {}

            try:
                y_true = self.models[ticker]['y_test']
                X_test = self.models[ticker]['X_test']

                # Evaluate base models
                base_models = ['RandomForest', 'XGBoost', 'LightGBM', 'CatBoost', 'GradientBoosting', 'ExtraTrees']
                for model_name in base_models:
                    if model_name in self.models[ticker]:
                        try:
                            pred = self.models[ticker][model_name].predict(X_test)
                            self.results[ticker][model_name] = self._calculate_metrics(y_true, pred, model_name)
                        except:
                            continue

                # Evaluate LSTM
                if 'LSTM' in self.models[ticker]:
                    try:
                        X_test_seq, y_test_seq = self.models[ticker]['LSTM_test_data']
                        pred = self.models[ticker]['LSTM'].predict(X_test_seq, verbose=0)
                        pred_binary = (pred > 0.5).astype(int).flatten()
                        self.results[ticker]['LSTM'] = self._calculate_metrics(y_test_seq, pred_binary, 'LSTM')
                    except:
                        pass

                # Evaluate ensemble models
                ensemble_models = ['VotingEnsemble', 'WeightedVoting', 'MetaRF']
                for model_name in ensemble_models:
                    if model_name in self.models[ticker]:
                        try:
                            pred = self.models[ticker][model_name]['predictions']
                            y_eval = y_true[:len(pred)]
                            self.results[ticker][model_name] = self._calculate_metrics(y_eval, pred, model_name)
                        except:
                            continue

            except Exception as e:
                print(f"    Error evaluating {ticker}: {e}")

        return self.results

    def _calculate_metrics(self, y_true, y_pred, model_name):
        """Calculate comprehensive performance metrics"""
        try:
            min_len = min(len(y_true), len(y_pred))
            y_true = y_true[:min_len]
            y_pred = y_pred[:min_len]

            acc = accuracy_score(y_true, y_pred)
            prec = precision_score(y_true, y_pred, zero_division=0)
            rec = recall_score(y_true, y_pred, zero_division=0)
            f1 = f1_score(y_true, y_pred, zero_division=0)
            cm = confusion_matrix(y_true, y_pred)

            status = self._get_performance_status(acc)
            print(f"    {model_name:15}: Accuracy={acc:.4f} ({acc*100:.1f}%), F1={f1:.4f} - {status}")

            return {
                'accuracy': acc, 'precision': prec, 'recall': rec, 'f1': f1,
                'confusion_matrix': cm, 'predictions': y_pred, 'status': status
            }
        except Exception as e:
            print(f"    Error calculating metrics for {model_name}: {e}")
            return {'accuracy': 0.0, 'precision': 0.0, 'recall': 0.0, 'f1': 0.0,
                   'confusion_matrix': np.zeros((2,2)), 'predictions': y_pred, 'status': 'ERROR'}

