    def evaluate_stacking_models(self, ticker, y_true):
        """Evaluate stacking models"""
        if ticker not in self.stacking_models:
            return

        X_meta, y_meta = self.stacking_models[ticker]['meta_data']

        for model_name in ['RF', 'GBT', 'AdaBoost', 'Voting']:
            if model_name in self.stacking_models[ticker]:
                pred = self.stacking_models[ticker][model_name].predict(X_meta)
                self.results[ticker][f'Stacking_{model_name}'] = self.calculate_metrics(
                    y_meta, pred, ticker, f'Stacking_{model_name}'
                )

    def calculate_metrics(self, y_true, y_pred, ticker, model_name):
        """Calculate comprehensive metrics"""
        acc = accuracy_score(y_true, y_pred)
        prec = precision_score(y_true, y_pred, zero_division=0)
        rec = recall_score(y_true, y_pred, zero_division=0)
        f1 = f1_score(y_true, y_pred, zero_division=0)
        cm = confusion_matrix(y_true, y_pred)

        print(f"    {model_name}: Acc={acc:.4f}, Prec={prec:.4f}, Rec={rec:.4f}, F1={f1:.4f}")

        return {
            'accuracy': acc,
            'precision': prec,
            'recall': rec,
            'f1': f1,
            'confusion_matrix': cm,
            'predictions': y_pred
        }

