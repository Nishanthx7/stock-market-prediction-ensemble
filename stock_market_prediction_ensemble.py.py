    # ---- Enhanced Technical Indicators ----
    def calculate_indicators(self, df):
        """Calculate comprehensive technical indicators as per the paper"""
        df = df.copy()

        # Basic price features
        df['Returns'] = df['Close'].pct_change()
        df['High_Low'] = (df['High'] - df['Low']) / df['Close']
        df['Price_Change'] = df['Close'].pct_change()

        # Moving Averages
        df['SMA_5'] = df['Close'].rolling(window=5).mean()
        df['SMA_10'] = df['Close'].rolling(window=10).mean()
        df['SMA_20'] = df['Close'].rolling(window=20).mean()
        df['SMA_50'] = df['Close'].rolling(window=50).mean()

        # Exponential Moving Averages (as per paper)
        df['EMA_12'] = df['Close'].ewm(span=12).mean()
        df['EMA_26'] = df['Close'].ewm(span=26).mean()

        # MACD
        df['MACD'] = df['EMA_12'] - df['EMA_26']
        df['MACD_Signal'] = df['MACD'].ewm(span=9).mean()
        df['MACD_Histogram'] = df['MACD'] - df['MACD_Signal']

        # RSI
        delta = df['Close'].diff()
        gain = delta.where(delta > 0, 0)
        loss = -delta.where(delta < 0, 0)
        avg_gain = gain.rolling(window=14).mean()
        avg_loss = loss.rolling(window=14).mean()
        rs = avg_gain / avg_loss
        df['RSI'] = 100 - (100 / (1 + rs))

        # Bollinger Bands (as per paper)
        df['BB_Middle'] = df['Close'].rolling(window=20).mean()
        bb_std = df['Close'].rolling(window=20).std()
        df['BB_Upper'] = df['BB_Middle'] + (bb_std * 2)
        df['BB_Lower'] = df['BB_Middle'] - (bb_std * 2)
        df['BB_Width'] = (df['BB_Upper'] - df['BB_Lower']) / df['BB_Middle']
        df['BB_Position'] = (df['Close'] - df['BB_Lower']) / (df['BB_Upper'] - df['BB_Lower'])

        # Stochastic Oscillator (as per paper)
        low_min = df['Low'].rolling(window=14).min()
        high_max = df['High'].rolling(window=14).max()
        df['Stochastic_K'] = 100 * ((df['Close'] - low_min) / (high_max - low_min))
        df['Stochastic_D'] = df['Stochastic_K'].rolling(window=3).mean()  # Slow stochastic

        # Parabolic SAR approximation (simplified)
        df['PSAR'] = df['Close'].shift(1)  # Simplified implementation

        # Volume indicators
        df['Volume_SMA'] = df['Volume'].rolling(window=20).mean()
        df['Volume_Ratio'] = df['Volume'] / df['Volume_SMA']

        # Price position indicators
        df['Price_Position'] = (df['Close'] - df['Low'].rolling(20).min()) / (df['High'].rolling(20).max() - df['Low'].rolling(20).min())

        # Momentum indicators
        df['Momentum_5'] = df['Close'] / df['Close'].shift(5) - 1
        df['Momentum_10'] = df['Close'] / df['Close'].shift(10) - 1

        # Volatility
        df['Volatility'] = df['Returns'].rolling(window=20).std()

        return df

    # ---- Feature Engineering ----
    def create_features_and_targets(self):
        """Create feature matrix and binary targets"""
        for ticker, df in self.data.items():
            # Calculate technical indicators
            df_indicators = self.calculate_indicators(df)

            # Create binary target (1 if next day's close > today's close)
            df_indicators['Target'] = (df_indicators['Close'].shift(-1) > df_indicators['Close']).astype(int)

            # Select features
            feature_columns = [
                'Open', 'High', 'Low', 'Close', 'Volume',
                'Returns', 'High_Low', 'Price_Change',
                'SMA_5', 'SMA_10', 'SMA_20', 'SMA_50',
                'EMA_12', 'EMA_26', 'MACD', 'MACD_Signal', 'MACD_Histogram',
                'RSI', 'BB_Middle', 'BB_Upper', 'BB_Lower', 'BB_Width', 'BB_Position',
                'Stochastic_K', 'Stochastic_D', 'PSAR',
                'Volume_SMA', 'Volume_Ratio', 'Price_Position',
                'Momentum_5', 'Momentum_10', 'Volatility'
            ]

            # Create feature dataframe
            available_features = [col for col in feature_columns if col in df_indicators.columns]
            features_df = df_indicators[available_features + ['Target']].dropna()

            # Handle any remaining missing values
            if features_df.isnull().sum().sum() > 0:
                imputer = SimpleImputer(strategy='median')
                features_df[available_features] = imputer.fit_transform(features_df[available_features])

            self.features[ticker] = features_df
            print(f"  ✓ Created features for {ticker}: {len(features_df)} samples, {len(available_features)} features")

        return self.features

    # ---- Base Models ----
    def train_base_models(self):
        """Train base models: ARIMA, GBT, RF, LSTM"""
        for ticker, df in self.features.items():
            print(f"\nTraining base models for {ticker}...")

            # Prepare data
            feature_cols = [col for col in df.columns if col != 'Target']
            X = df[feature_cols].values
            y = df['Target'].values

            # Train-test split
            split_idx = int(len(X) * (1 - self.test_size))
            X_train, X_test = X[:split_idx], X[split_idx:]
            y_train, y_test = y[:split_idx], y[split_idx:]

            # Scale features
            scaler = StandardScaler()
            X_train_scaled = scaler.fit_transform(X_train)
            X_test_scaled = scaler.transform(X_test)

            self.models[ticker] = {
                'scaler': scaler,
                'X_test': X_test_scaled,
                'y_test': y_test,
                'feature_cols': feature_cols
            }

            # 1. ARIMA Model (for price prediction, then convert to binary)
            try:
                start_time = time.time()
                price_series = df['Close'][:split_idx]
                arima_model = ARIMA(price_series, order=(1, 1, 1))
                arima_fitted = arima_model.fit()

                # Predict on test set
                arima_predictions = []
                for i in range(len(X_test)):
                    pred = arima_fitted.forecast(steps=1)[0]
                    current_price = df['Close'].iloc[split_idx + i - 1]
                    binary_pred = 1 if pred > current_price else 0
                    arima_predictions.append(binary_pred)

                self.models[ticker]['ARIMA'] = arima_fitted
                self.models[ticker]['ARIMA_predictions'] = arima_predictions
                self.execution_times[f'{ticker}_ARIMA'] = time.time() - start_time
                print(f"    ✓ ARIMA trained ({self.execution_times[f'{ticker}_ARIMA']:.2f}s)")
            except Exception as e:
                print(f"    ✗ ARIMA failed: {e}")
                self.models[ticker]['ARIMA_predictions'] = [0] * len(X_test)

            # 2. Gradient Boosting Trees
            start_time = time.time()
            gbt = GradientBoostingClassifier(
                n_estimators=150,
                max_depth=5,
                learning_rate=0.1,
                random_state=42
            )
            gbt.fit(X_train_scaled, y_train)
            self.models[ticker]['GBT'] = gbt
            self.execution_times[f'{ticker}_GBT'] = time.time() - start_time
            print(f"    ✓ GBT trained ({self.execution_times[f'{ticker}_GBT']:.2f}s)")

            # 3. Random Forest
            start_time = time.time()
            rf = RandomForestClassifier(
                n_estimators=200,
                max_depth=7,
                random_state=42,
                n_jobs=-1
            )
            rf.fit(X_train_scaled, y_train)
            self.models[ticker]['RF'] = rf
            self.execution_times[f'{ticker}_RF'] = time.time() - start_time
            print(f"    ✓ Random Forest trained ({self.execution_times[f'{ticker}_RF']:.2f}s)")

            # 4. LSTM Model
            start_time = time.time()
            if len(X_train) > 60:  # Ensure enough data for sequences
                # Prepare sequence data for LSTM
                sequence_length = 20
                X_seq_train, y_seq_train = self.create_sequences(X_train_scaled, y_train, sequence_length)
                X_seq_test, y_seq_test = self.create_sequences(X_test_scaled, y_test, sequence_length)

                if len(X_seq_train) > 0:
                    lstm_model = self.build_lstm_model((sequence_length, X_train_scaled.shape[1]))

                    early_stopping = EarlyStopping(monitor='val_loss', patience=10, restore_best_weights=True)
                    lstm_model.fit(
                        X_seq_train, y_seq_train,
                        epochs=50, batch_size=32,
                        validation_data=(X_seq_test, y_seq_test),
                        verbose=0, callbacks=[early_stopping]
                    )

                    self.models[ticker]['LSTM'] = lstm_model
                    self.models[ticker]['LSTM_test_data'] = (X_seq_test, y_seq_test)
                    print(f"    ✓ LSTM trained ({time.time() - start_time:.2f}s)")
                else:
                    print("    ✗ LSTM: Not enough sequence data")
            else:
                print("    ✗ LSTM: Insufficient training data")

            self.execution_times[f'{ticker}_LSTM'] = time.time() - start_time

        return self.models

    def create_sequences(self, X, y, sequence_length):
        """Create sequences for LSTM"""
        X_seq, y_seq = [], []
        for i in range(sequence_length, len(X)):
            X_seq.append(X[i-sequence_length:i])
            y_seq.append(y[i])
        return np.array(X_seq), np.array(y_seq)

    def build_lstm_model(self, input_shape):
        """Build LSTM model architecture"""
        model = Sequential([
            LSTM(50, return_sequences=True, input_shape=input_shape),
            Dropout(0.2),
            LSTM(25),
            Dropout(0.2),
            Dense(10, activation='relu'),
            Dense(1, activation='sigmoid')
        ])

        model.compile(
            optimizer=Adam(learning_rate=0.001),
            loss='binary_crossentropy',
            metrics=['accuracy']
        )
        return model

