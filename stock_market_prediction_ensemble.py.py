    def simulate_trading(self, price_data, predictions, initial_budget, max_shares):
        """Simulate trading based on predictions"""
        budget = initial_budget
        shares_owned = 0
        portfolio_value = []
        trades = []

        # Align predictions and price data
        min_len = min(len(price_data) - 1, len(predictions))  # -1 because we need next day's price

        for i in range(min_len):
            current_price = price_data.iloc[i]
            next_day_price = price_data.iloc[i + 1] if i + 1 < len(price_data) else current_price
            prediction = predictions[i]

            if prediction == 1:  # Buy signal
                # Buy shares if we have budget
                shares_to_buy = min(max_shares, int(budget / current_price))
                if shares_to_buy > 0:
                    cost = shares_to_buy * current_price
                    budget -= cost
                    shares_owned += shares_to_buy
                    trades.append(('BUY', i, shares_to_buy, current_price))
            else:  # Sell signal
                # Sell shares if we have any
                shares_to_sell = min(max_shares, shares_owned)
                if shares_to_sell > 0:
                    revenue = shares_to_sell * current_price
                    budget += revenue
                    shares_owned -= shares_to_sell
                    trades.append(('SELL', i, shares_to_sell, current_price))

            # Calculate current portfolio value
            current_portfolio_value = budget + (shares_owned * current_price)
            portfolio_value.append(current_portfolio_value)

        # Final portfolio value (sell all remaining shares)
        if shares_owned > 0 and len(price_data) > 0:
            final_price = price_data.iloc[-1]
            budget += shares_owned * final_price
            shares_owned = 0

        final_value = budget
        total_return = final_value - initial_budget
        return_percentage = (total_return / initial_budget) * 100

        return {
            'initial_budget': initial_budget,
            'final_value': final_value,
            'total_return': total_return,
            'return_percentage': return_percentage,
            'trades': trades,
            'portfolio_values': portfolio_value
        }

