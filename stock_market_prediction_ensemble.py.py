    def create_sequences(self, X, y, sequence_length):
        """Create sequences for LSTM with error handling"""
        try:
            X_seq, y_seq = [], []
            for i in range(sequence_length, len(X)):
                X_seq.append(X[i-sequence_length:i])
                y_seq.append(y[i])
            return np.array(X_seq), np.array(y_seq)
        except Exception as e:
            print(f"      Warning: Error creating sequences - {e}")
            return np.array([]), np.array([])

    def train_ensemble_models(self):
        """Train ensemble models using base model predictions"""
        print("🎯 Training ensemble models...")

        for ticker in self.models.keys():
            if ticker not in self.models:
                continue

            try:
                print(f"  Creating ensembles for {ticker}...")

                # Get base model predictions
                base_predictions = []
                X_test = self.models[ticker]['X_test']
                y_test = self.models[ticker]['y_test']

                model_names = []

                # Collect predictions from base models
                base_model_names = ['RandomForest', 'XGBoost', 'LightGBM', 'CatBoost', 'GradientBoosting', 'ExtraTrees']
                for model_name in base_model_names:
                    if model_name in self.models[ticker]:
                        try:
                            pred = self.models[ticker][model_name].predict(X_test)
                            base_predictions.append(pred)
                            model_names.append(model_name)
                        except Exception as e:
                            print(f"    ⚠️  Could not get predictions from {model_name}: {e}")

                # LSTM predictions
                if 'LSTM' in self.models[ticker] and 'LSTM_test_data' in self.models[ticker]:
                    try:
                        X_test_seq, y_test_seq = self.models[ticker]['LSTM_test_data']
                        lstm_pred = self.models[ticker]['LSTM'].predict(X_test_seq)
                        lstm_pred_binary = (lstm_pred > 0.5).astype(int).flatten()

                        # Align lengths
                        if len(base_predictions) > 0:
                            min_len = min(len(base_predictions[0]), len(lstm_pred_binary))
                            base_predictions = [pred[:min_len] for pred in base_predictions]
                            y_test = y_test[:min_len]
                            base_predictions.append(lstm_pred_binary[:min_len])
                            model_names.append('LSTM')
                        else:
                            base_predictions = [lstm_pred_binary]
                            y_test = y_test_seq
                            model_names = ['LSTM']
                    except Exception as e:
                        print(f"    ⚠️  Could not process LSTM predictions: {e}")

                if len(base_predictions) >= 2:
                    # Create meta-features
                    X_meta = np.column_stack(base_predictions)

                    # Simple Voting Ensemble
                    try:
                        voting_pred = (np.mean(X_meta, axis=1) > 0.5).astype(int)
                        self.models[ticker]['VotingEnsemble'] = {
                            'predictions': voting_pred,
                            'method': 'simple_voting'
                        }
                        print(f"    ✅ Voting Ensemble created")
                    except Exception as e:
                        print(f"    ❌ Voting Ensemble failed: {e}")

                    # Weighted Voting based on individual accuracy
                    try:
                        weights = []
                        for i, pred in enumerate(base_predictions):
                            acc = accuracy_score(y_test[:len(pred)], pred)
                            weights.append(max(acc, 0.5))  # Minimum weight of 0.5

                        weights = np.array(weights) / np.sum(weights)
                        weighted_pred = (np.average(X_meta, axis=1, weights=weights) > 0.5).astype(int)

                        self.models[ticker]['WeightedVoting'] = {
                            'predictions': weighted_pred,
                            'weights': weights,
                            'method': 'weighted_voting'
                        }
                        print(f"    ✅ Weighted Voting created")
                    except Exception as e:
                        print(f"    ❌ Weighted Voting failed: {e}")

                    # Meta-learner with Random Forest
                    try:
                        meta_rf = RandomForestClassifier(
                            n_estimators=50,
                            max_depth=5,
                            random_state=42
                        )
                        meta_rf.fit(X_meta, y_test)
                        meta_pred = meta_rf.predict(X_meta)

                        self.models[ticker]['MetaRF'] = {
                            'model': meta_rf,
                            'predictions': meta_pred
                        }
                        print(f"    ✅ Meta Random Forest created")
                    except Exception as e:
                        print(f"    ❌ Meta Random Forest failed: {e}")

                    # Store meta data
                    self.models[ticker]['meta_data'] = {
                        'X_meta': X_meta,
                        'y_meta': y_test,
                        'base_model_names': model_names
                    }
                else:
                    print(f"    ⚠️  Not enough base models for ensemble (need at least 2)")

            except Exception as e:
                print(f"    ❌ Error creating ensembles for {ticker}: {e}")

        return self.models

    def evaluate_models(self):
        """Evaluate all models with comprehensive error handling"""
        print("📈 Evaluating models...")

        for ticker in self.models.keys():
            if ticker not in self.models:
                continue

            print(f"  Evaluating {ticker}...")
            self.results[ticker] = {}

            try:
                y_true = self.models[ticker]['y_test']
                X_test = self.models[ticker]['X_test']

                # Evaluate base models
                base_model_names = ['RandomForest', 'XGBoost', 'LightGBM', 'CatBoost', 'GradientBoosting', 'ExtraTrees']
                for model_name in base_model_names:
                    if model_name in self.models[ticker]:
                        try:
                            pred = self.models[ticker][model_name].predict(X_test)
                            metrics = self.calculate_metrics(y_true, pred, model_name)
                            self.results[ticker][model_name] = metrics
                        except Exception as e:
                            print(f"    ⚠️  Error evaluating {model_name}: {e}")

                # Evaluate LSTM
                if 'LSTM' in self.models[ticker] and 'LSTM_test_data' in self.models[ticker]:
                    try:
                        X_test_seq, y_test_seq = self.models[ticker]['LSTM_test_data']
                        pred = self.models[ticker]['LSTM'].predict(X_test_seq)
                        pred_binary = (pred > 0.5).astype(int).flatten()
                        metrics = self.calculate_metrics(y_test_seq, pred_binary, 'LSTM')
                        self.results[ticker]['LSTM'] = metrics
                    except Exception as e:
                        print(f"    ⚠️  Error evaluating LSTM: {e}")

                # Evaluate ensemble models
                ensemble_names = ['VotingEnsemble', 'WeightedVoting', 'MetaRF']
                for ensemble_name in ensemble_names:
                    if ensemble_name in self.models[ticker]:
                        try:
                            if 'predictions' in self.models[ticker][ensemble_name]:
                                pred = self.models[ticker][ensemble_name]['predictions']
                                # Align y_true length with predictions
                                y_eval = y_true[:len(pred)]
                                metrics = self.calculate_metrics(y_eval, pred, ensemble_name)
                                self.results[ticker][ensemble_name] = metrics
                            elif 'model' in self.models[ticker][ensemble_name]:
                                meta_data = self.models[ticker]['meta_data']
                                pred = self.models[ticker][ensemble_name]['predictions']
                                y_eval = meta_data['y_meta'][:len(pred)]
                                metrics = self.calculate_metrics(y_eval, pred, ensemble_name)
                                self.results[ticker][ensemble_name] = metrics
                        except Exception as e:
                            print(f"    ⚠️  Error evaluating {ensemble_name}: {e}")

            except Exception as e:
                print(f"    ❌ Error evaluating models for {ticker}: {e}")

        return self.results

    def calculate_metrics(self, y_true, y_pred, model_name):
        """Calculate metrics with error handling"""
        try:
            # Ensure same length
            min_len = min(len(y_true), len(y_pred))
            y_true = y_true[:min_len]
            y_pred = y_pred[:min_len]

            acc = accuracy_score(y_true, y_pred)
            prec = precision_score(y_true, y_pred, zero_division=0)
            rec = recall_score(y_true, y_pred, zero_division=0)
            f1 = f1_score(y_true, y_pred, zero_division=0)
            cm = confusion_matrix(y_true, y_pred)

            # Status based on accuracy
            if acc >= 0.70:
                status = "🎯 TARGET ACHIEVED!"
            elif acc >= 0.65:
                status = "✅ VERY GOOD"
            elif acc >= 0.60:
                status = "👍 GOOD"
            elif acc >= 0.55:
                status = "⚠️  FAIR"
            else:
                status = "❌ POOR"

            print(f"    {model_name:15}: Acc={acc:.4f} ({acc*100:.1f}%), F1={f1:.4f} {status}")

            return {
                'accuracy': acc,
                'precision': prec,
                'recall': rec,
                'f1': f1,
                'confusion_matrix': cm,
                'predictions': y_pred,
                'status': status
            }

        except Exception as e:
            print(f"    ❌ Error calculating metrics for {model_name}: {e}")
            return {
                'accuracy': 0.0,
                'precision': 0.0,
                'recall': 0.0,
                'f1': 0.0,
                'confusion_matrix': np.array([[0, 0], [0, 0]]),
                'predictions': y_pred,
                'status': "❌ ERROR"
            }

