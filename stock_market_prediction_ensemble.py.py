    def get_base_model_predictions(self, ticker):
        """Get predictions from all base models"""
        try:
            predictions = []

            # GBT predictions
            gbt_pred = self.models[ticker]['GBT'].predict(self.models[ticker]['X_test'])
            predictions.append(gbt_pred)

            # RF predictions
            rf_pred = self.models[ticker]['RF'].predict(self.models[ticker]['X_test'])
            predictions.append(rf_pred)

            # ARIMA predictions
            if 'ARIMA_predictions' in self.models[ticker]:
                arima_pred = np.array(self.models[ticker]['ARIMA_predictions'])
                # Ensure same length
                min_len = min(len(gbt_pred), len(arima_pred))
                predictions[0] = predictions[0][:min_len]
                predictions[1] = predictions[1][:min_len]
                predictions.append(arima_pred[:min_len])

            # LSTM predictions
            if 'LSTM' in self.models[ticker] and 'LSTM_test_data' in self.models[ticker]:
                X_seq_test, _ = self.models[ticker]['LSTM_test_data']
                lstm_pred = self.models[ticker]['LSTM'].predict(X_seq_test)
                lstm_pred_binary = (lstm_pred > 0.5).astype(int).flatten()

                # Adjust lengths
                min_len = min(len(predictions[0]), len(lstm_pred_binary))
                for i in range(len(predictions)):
                    predictions[i] = predictions[i][:min_len]
                predictions.append(lstm_pred_binary[:min_len])

            # Stack predictions horizontally
            if len(predictions) > 1:
                base_predictions = np.column_stack(predictions)
                return base_predictions
            else:
                return None

        except Exception as e:
            print(f"    ✗ Error getting base predictions: {e}")
            return None

