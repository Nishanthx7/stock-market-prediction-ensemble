    def calculate_technical_indicators(self, df):
        """Calculate technical indicators with error handling"""
        df = df.copy()

        try:
            # Basic price features
            df['Returns'] = df['Close'].pct_change().fillna(0)
            df['High_Low_Ratio'] = df['High'] / df['Low']
            df['Open_Close_Ratio'] = df['Open'] / df['Close']

            # Price changes
            df['Price_Change_1'] = df['Close'].pct_change(1).fillna(0)
            df['Price_Change_2'] = df['Close'].pct_change(2).fillna(0)
            df['Price_Change_5'] = df['Close'].pct_change(5).fillna(0)

            # Moving Averages - Calculate step by step to avoid KeyError
            periods = [5, 10, 15, 20, 30, 50]
            for period in periods:
                if len(df) > period:
                    df[f'SMA_{period}'] = df['Close'].rolling(window=period, min_periods=1).mean()
                    df[f'EMA_{period}'] = df['Close'].ewm(span=period, min_periods=1).mean()
                    df[f'Price_SMA_{period}_Ratio'] = df['Close'] / df[f'SMA_{period}']

            # RSI - Multiple periods
            for period in [7, 14, 21]:
                if len(df) > period:
                    delta = df['Close'].diff()
                    gain = delta.where(delta > 0, 0).rolling(window=period, min_periods=1).mean()
                    loss = -delta.where(delta < 0, 0).rolling(window=period, min_periods=1).mean()
                    rs = gain / (loss + 1e-10)  # Add small value to avoid division by zero
                    df[f'RSI_{period}'] = 100 - (100 / (1 + rs))

            # MACD - Only if we have EMA_12 and EMA_26
            if 'EMA_12' in df.columns and 'EMA_26' in df.columns:
                df['MACD'] = df['EMA_12'] - df['EMA_26']
                df['MACD_Signal'] = df['MACD'].ewm(span=9, min_periods=1).mean()
                df['MACD_Histogram'] = df['MACD'] - df['MACD_Signal']

            # Bollinger Bands
            if len(df) > 20:
                bb_period = 20
                df['BB_Middle'] = df['Close'].rolling(window=bb_period, min_periods=1).mean()
                bb_std = df['Close'].rolling(window=bb_period, min_periods=1).std()
                df['BB_Upper'] = df['BB_Middle'] + (bb_std * 2)
                df['BB_Lower'] = df['BB_Middle'] - (bb_std * 2)
                df['BB_Width'] = (df['BB_Upper'] - df['BB_Lower']) / df['BB_Middle']
                df['BB_Position'] = (df['Close'] - df['BB_Lower']) / (df['BB_Upper'] - df['BB_Lower'])

            # Stochastic Oscillator
            if len(df) > 14:
                period = 14
                low_min = df['Low'].rolling(window=period, min_periods=1).min()
                high_max = df['High'].rolling(window=period, min_periods=1).max()
                df['Stoch_K'] = 100 * ((df['Close'] - low_min) / (high_max - low_min + 1e-10))
                df['Stoch_D'] = df['Stoch_K'].rolling(window=3, min_periods=1).mean()

            # Williams %R
            if len(df) > 14:
                period = 14
                high_max = df['High'].rolling(window=period, min_periods=1).max()
                low_min = df['Low'].rolling(window=period, min_periods=1).min()
                df['Williams_R'] = -100 * ((high_max - df['Close']) / (high_max - low_min + 1e-10))

            # Average True Range (ATR)
            if len(df) > 14:
                high_low = df['High'] - df['Low']
                high_close = abs(df['High'] - df['Close'].shift(1))
                low_close = abs(df['Low'] - df['Close'].shift(1))

                true_range = pd.concat([high_low, high_close, low_close], axis=1).max(axis=1)
                df['ATR'] = true_range.rolling(window=14, min_periods=1).mean()
                df['ATR_Ratio'] = df['ATR'] / df['Close']

            # Volume indicators
            df['Volume_SMA_20'] = df['Volume'].rolling(window=20, min_periods=1).mean()
            df['Volume_Ratio'] = df['Volume'] / df['Volume_SMA_20']
            df['Volume_Price_Trend'] = df['Volume'] * df['Returns']

            # Volatility
            df['Volatility_10'] = df['Returns'].rolling(window=10, min_periods=1).std()
            df['Volatility_20'] = df['Returns'].rolling(window=20, min_periods=1).std()

            # Price position
            df['Price_Position_20'] = ((df['Close'] - df['Low'].rolling(20, min_periods=1).min()) /
                                     (df['High'].rolling(20, min_periods=1).max() - df['Low'].rolling(20, min_periods=1).min() + 1e-10))

            # Momentum indicators
            df['Momentum_5'] = df['Close'] / df['Close'].shift(5) - 1
            df['Momentum_10'] = df['Close'] / df['Close'].shift(10) - 1

            # Fill any remaining NaN values
            df = df.fillna(method='forward').fillna(method='backward').fillna(0)

        except Exception as e:
            print(f"    Warning: Error calculating some indicators - {e}")

        return df

    def create_features(self):
        """Create features with robust error handling"""
        print("🛠️  Creating features...")

        for ticker, df in self.data.items():
            try:
                print(f"  Processing {ticker}...")

                # Calculate technical indicators
                df_with_indicators = self.calculate_technical_indicators(df)

                # Create target variable
                df_with_indicators['Target'] = (df_with_indicators['Close'].shift(-1) > df_with_indicators['Close']).astype(int)

                # Get all numeric columns except the target
                numeric_cols = df_with_indicators.select_dtypes(include=[np.number]).columns.tolist()
                feature_cols = [col for col in numeric_cols if col not in ['Target', 'Close']]

                # Remove any columns that might have issues
                safe_feature_cols = []
                for col in feature_cols:
                    try:
                        if not df_with_indicators[col].isna().all() and df_with_indicators[col].std() > 1e-10:
                            safe_feature_cols.append(col)
                    except:
                        continue

                # Create final feature dataframe
                final_cols = safe_feature_cols + ['Target']
                features_df = df_with_indicators[final_cols].copy()

                # Remove rows with NaN target (last row)
                features_df = features_df.dropna(subset=['Target'])

                # Handle any remaining missing values
                if features_df.isnull().sum().sum() > 0:
                    # Fill with median for numeric columns
                    for col in safe_feature_cols:
                        if features_df[col].isnull().sum() > 0:
                            median_val = features_df[col].median()
                            features_df[col] = features_df[col].fillna(median_val)

                # Remove highly correlated features to avoid multicollinearity
                if len(safe_feature_cols) > 10:
                    corr_matrix = features_df[safe_feature_cols].corr().abs()
                    upper_tri = corr_matrix.where(np.triu(np.ones(corr_matrix.shape), k=1).astype(bool))
                    high_corr_features = [column for column in upper_tri.columns if any(upper_tri[column] > 0.95)]
                    safe_feature_cols = [col for col in safe_feature_cols if col not in high_corr_features]

                # Feature selection if we have too many features
                if len(safe_feature_cols) > 30:
                    X = features_df[safe_feature_cols]
                    y = features_df['Target']

                    # Use SelectKBest to get top features
                    selector = SelectKBest(score_func=f_classif, k=min(30, len(safe_feature_cols)))
                    X_selected = selector.fit_transform(X, y)
                    selected_features = [safe_feature_cols[i] for i in selector.get_support(indices=True)]
                else:
                    selected_features = safe_feature_cols

                # Final feature dataframe
                self.features[ticker] = features_df[selected_features + ['Target']].copy()

                print(f"    ✅ Created {len(selected_features)} features, {len(self.features[ticker])} samples")

            except Exception as e:
                print(f"    ❌ Error processing {ticker}: {e}")
                continue

        if not self.features:
            raise ValueError("No features could be created!")

        return self.features

