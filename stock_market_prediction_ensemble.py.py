    def generate_comprehensive_report(self):
        """Generate comprehensive analysis report"""
        print("\\n" + "=" * 80)
        print("COMPREHENSIVE STOCK PREDICTION ANALYSIS REPORT")
        print("=" * 80)

        # Model Performance Summary
        print("\\n1. MODEL PERFORMANCE SUMMARY")
        print("-" * 50)

        all_accuracies = []
        models_70_plus = []
        best_models = {}

        for ticker, results in self.results.items():
            print(f"\\n{ticker} PERFORMANCE:")
            print("-" * 30)

            sorted_models = sorted(results.items(), key=lambda x: x[1]['accuracy'], reverse=True)

            for model_name, metrics in sorted_models:
                acc = metrics['accuracy']
                f1 = metrics['f1']
                status = metrics['status']
                all_accuracies.append(acc)

                print(f"  {model_name:20}: Acc={acc:.4f} ({acc*100:.1f}%), F1={f1:.4f} - {status}")

                if acc >= 0.70:
                    models_70_plus.append((ticker, model_name, acc))

            if sorted_models:
                best_model = sorted_models[0]
                best_models[ticker] = best_model
                print(f"\\n  BEST MODEL: {best_model[0]} - {best_model[1]['accuracy']*100:.1f}%")

        # Overall Statistics
        print("\\n2. OVERALL PERFORMANCE STATISTICS")
        print("-" * 50)

        if all_accuracies:
            print(f"Best Overall Accuracy: {max(all_accuracies):.4f} ({max(all_accuracies)*100:.1f}%)")
            print(f"Average Accuracy: {np.mean(all_accuracies):.4f} ({np.mean(all_accuracies)*100:.1f}%)")
            print(f"Models with 70%+ accuracy: {len(models_70_plus)}/{len(all_accuracies)}")
            print(f"Models with 65%+ accuracy: {sum(1 for acc in all_accuracies if acc >= 0.65)}")
            print(f"Models with 60%+ accuracy: {sum(1 for acc in all_accuracies if acc >= 0.60)}")

            if models_70_plus:
                print(f"\\nMODELS ACHIEVING 70%+ ACCURACY:")
